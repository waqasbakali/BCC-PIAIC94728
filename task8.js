var num = prompt('Enter number, positive/negative/zero');

if( num >0){
    alert('Positive number')
   document.write('Positive number')                    
}else if(num <0){
    alert('Negative number')
   document.write('Negative number')                    
}else if(num == 0){
    alert('Zero number')
   document.write('Zero number')                    
}else{
    alert('You entered invalid number')
   document.write('You entered invalid number')                    
}